   private void populateFields() {
    int selectedRow = jTable1.getSelectedRow();
    int columnCount = jTable1.getColumnCount(); // Jumlah kolom di tabel

    if (selectedRow != -1) { // Pastikan ada baris yang dipilih
        if (columnCount >= 6) { // Pastikan jumlah kolom cukup (sesuai kolom yang digunakan)
            // Isi field dengan data dari tabel
            jTextIdReservasi.setText(jTable1.getValueAt(selectedRow, 0).toString());
            jTextIdPengunjung.setText(jTable1.getValueAt(selectedRow, 1).toString());
            jTextIdDestinasi.setText(jTable1.getValueAt(selectedRow, 2).toString());
            jTextTanggalReservasi.setText(jTable1.getValueAt(selectedRow, 3).toString());
            jTextJumlahTiket.setText(jTable1.getValueAt(selectedRow, 4).toString());
            jTextTotal.setText(jTable1.getValueAt(selectedRow, 5).toString());
        } else {
            // Tampilkan pesan error jika jumlah kolom tidak cukup
            JOptionPane.showMessageDialog(this, 
                "Jumlah kolom tabel tidak sesuai. Harus minimal 5 kolom.", 
                "Error", JOptionPane.ERROR_MESSAGE);
        }
    } else {
        // Tampilkan pesan error jika tidak ada baris yang dipilih
        JOptionPane.showMessageDialog(this, 
            "Pilih baris di tabel terlebih dahulu.", 
            "Peringatan", JOptionPane.WARNING_MESSAGE);
    }
}
