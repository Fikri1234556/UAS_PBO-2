    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:
        try {
            String IdR = jTextIdReservasi.getText();
            String IdP = jTextIdPengunjung.getText();
            String IdD = jTextIdDestinasi.getText();
            String Tanggal = jTextTanggalReservasi.getText();
            String Jumlah = jTextJumlahTiket.getText();
            String Total = jTextTotal.getText();

            String[] fields = {"id_reservasi", "id_pengunjung", "id_destinasi", "tanggal_reservasi", "jumlah_tiket", "total_harga"};
            String[] values = {IdR, IdP, IdD, Tanggal, Jumlah, Total};

            crud.SimpanDinamis("reservasi", fields, values);
            JOptionPane.showMessageDialog(null, "Berhasil disimpan");
            loadData();
            clearFields();
        } catch (Exception e) {
            System.err.println("Terjadi kesalahan: " + e.getMessage());
        }
    }//GEN-LAST:event_jButton1ActionPerformed

